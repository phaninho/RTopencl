#ifndef RT_MATH
# define RT_MATH

# define ABS(n)			(n < 0 ? -n : n)
# define MAX(a,b)		((a) > (b) ? a : b)
# define MIN(a,b)		((a) < (b) ? a : b)
# define CLAMP(a,b,c)	((a < b) ? b : (a > c) ? c : a)

#endif
